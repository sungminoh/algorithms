class Solution:
    def shortestPath(self, grid: List[List[int]], k: int) -> int:
        """11/07/2022 09:39
        TLE
        """
        m, n = len(grid), len(grid[0])

        def neighbor(i, j):
            for dx, dy in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
                x, y = i+dx, j+dy
                if 0<=x<m and 0<=y<n:
                    yield x, y

        @lru_cache(None)
        def dfs(i, j, k, visited):
            if i == m-1 and j == n-1:
                return 0
            sub = m*n+1
            for x, y in neighbor(i, j):
                if visited & 1<<(n*x+y) > 0:
                    continue
                if grid[x][y] == 1:
                    if k>0:
                        sub = min(sub, dfs(x, y, k-1, visited | 1<<(n*x+y)))
                else:
                    sub = min(sub, dfs(x, y, k, visited | 1<<(n*x+y)))
            return sub+1

        ret = dfs(0, 0, k, 1)
        return ret if ret <= m*n else -1

    def shortestPath(self, grid: List[List[int]], k: int) -> int:
        m, n = len(grid), len(grid[0])

        def neighbor(i, j):
            for dx, dy in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
                x, y = i+dx, j+dy
                if 0<=x<m and 0<=y<n:
                    yield x, y

        memo = {}
        ret = m*n+1
        def dfs(i, j, k):
            if i == m-1 and j == n-1:
                return 0
            if (i, j, k) in memo:
                return memo[i, j, k]
            if grid[i][j] == 1 and k == 0:
                return m*n+1
            memo[i, j, k] = m*n+1
            for x, y in neighbor(i, j):
                memo[i, j, k] = min(memo[i, j, k], dfs(x, y, k-grid[i][j])+1)
            return memo[i, j, k]

        ret = dfs(0, 0, k)
        return ret if ret <= m*n else -1

    def shortestPath(self, grid: List[List[int]], k: int) -> int:
        """11/07/2022 10:09"""
        m, n = len(grid), len(grid[0])

        def neighbor(i, j):
            for dx, dy in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
                x, y = i+dx, j+dy
                if 0<=x<m and 0<=y<n:
                    yield x, y

        ret = m*n+1
        visited = set([(0, 0, k)])
        queue = deque([(0, 0, k, 0)])
        while queue:
            i, j, k, s = queue.popleft()
            if i == m-1 and j == n-1:
                return s
            for x, y in neighbor(i, j):
                if k >= grid[x][y] and (x, y, k-grid[x][y]) not in visited:
                    visited.add((x, y, k-grid[x][y]))
                    queue.append((x, y, k-grid[x][y], s+1))
        return -1


